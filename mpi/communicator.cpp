#include "communicator.hpp"

#include <iostream>

namespace Mpi
{
//####################################################################################################################
	Communicator::Communicator(Context* context, MPI_Comm com)
		: context_{context}
		, com_{com}
	{
		
	}
//--------------------------------------------------------------------------------------------------------------------
	Communicator::~Communicator()
	{
		if (com_ != MPI_COMM_WORLD)
		{
			MPI_Comm_free(&com_);
		}
	}
//--------------------------------------------------------------------------------------------------------------------
	MPI_Comm Communicator::getHandle() const
	{
		return com_;
	}
//####################################################################################################################
}
